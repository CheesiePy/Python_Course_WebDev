# working on time with python 

import time

# time module has a function called sleep() which is used to delay the execution of a function for a given number of seconds.

time.sleep(5) # this will delay the execution of the function for 5 seconds


# """time module has a function called time() which is used to get the current time in seconds since the epoch.
# The epoch is the point where the time starts, which is different for every system.

x = time.time()
print(x)

# """time module has a function called ctime() which is used to get the current time in a human-readable format.

x = time.ctime()
print(x)



